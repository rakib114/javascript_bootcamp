// Boolean & comparison operator

// booleans
// true & false

let num1 = 8,
  num2 = 8;

console.log(num1 >= num2);

// == vs ===

let num3 = "9",
  num4 = 9;

// == check only values not data type

console.log(num3 == num4);

// === check values and also data type;

console.log(num3 === num4);

// != vs !==

let num5 = 3,
  num6 = "3";

// != check only values not data types
console.log(num5 != num6);

// !== check values and alos data types
console.log(num5 !== num6);
