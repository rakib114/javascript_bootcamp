// rulse for naming variable

// we Can't start with number
// example :-
// 1value (invalied)
// value1(valied)
var value1 = 10;
console.log(value1);

// We can only use underscore _ or dollar $ symbol
// First_name (valied)
// _firstname (valied)

var First_name = "Mohit"; //> snake case writing
console.log(First_name);

// Last$name (valied)
// $Lastname (valied)
var $Lastname = "Sharma";
console.log($Lastname);

// We can't use space
// first name (invalid)

