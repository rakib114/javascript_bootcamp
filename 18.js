// Premitive VS reference data type

// Premitive Data type
let num1 = 5;
let num2 = num1;
console.log(num1);
console.log(num2);
// After incrementing num1 value
num1++;
console.log(num1);
console.log(num2);

// Reference data type

let array1 = ["Rohit", "Mohit"];
let array2 = array1;
console.log(array1);
console.log(array2);
// After pushin a iteam in array1
array1.push("Bhuvan");
console.log(array1);
console.log(array2);
